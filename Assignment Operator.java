
//Subtraction Operator

import java.io.*;
 

class Subtraction {

    public static void main(String[] args)

    {

        int num1 = 60, num2 = 23, sub = 0;


        System.out.println("num1 = " + num1);

        System.out.println("num2 = " + num2);
 

        sub = num1 - num2;

        System.out.println("Subtraction = " + sub);

    }
}
